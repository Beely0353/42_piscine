#include "../includes/rush01.h"

void	print_tab(char **tab)
{
	int	y;
	int	x;

	y = 1;
	while (y != 5)
	{
		x = 1;
		while (x != 5)
		{
			write(1, &tab[y][x], 1);
			if (x != 4)
				write(1, " ", 1);
			x++;
		}
		write(1, "\n", 1);
		y++;
	}
}
